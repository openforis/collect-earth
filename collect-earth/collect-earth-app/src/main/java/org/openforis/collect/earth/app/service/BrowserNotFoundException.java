package org.openforis.collect.earth.app.service;

public class BrowserNotFoundException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -404938492481063378L;

	public BrowserNotFoundException() {
		super();
	}


	public BrowserNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public BrowserNotFoundException(String message) {
		super(message);
	}

	public BrowserNotFoundException(Throwable cause) {
		super(cause);
	}

	
}
